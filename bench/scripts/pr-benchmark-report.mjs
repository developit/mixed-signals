import fs from 'node:fs/promises';

function pct(base, head) {
  if (!Number.isFinite(base) || base === 0) return 'n/a';
  const v = ((head - base) / base) * 100;
  return `${v >= 0 ? '+' : ''}${v.toFixed(2)}%`;
}

function fmt(n, digits = 2) {
  return Number.isFinite(n) ? n.toFixed(digits) : 'n/a';
}

function normalizeNode(report) {
  if (report?.missing || !Array.isArray(report?.results)) return [];
  return report.results.map((r) => ({
    key: `${r.scenario}|${r.mode}|${r.topology}`,
    scenario: r.scenario,
    opsPerSec: r.metrics.opsPerSec,
    p95: r.metrics.latency.p95,
  }));
}

function normalizeD8(report) {
  if (report?.missing || !Array.isArray(report?.results)) return [];
  return report.results.map((r) => ({
    key: r.scenario,
    scenario: r.scenario,
    opsPerSec: r.opsPerSec,
    p95: r.p95,
  }));
}

function table(title, baseRows, headRows) {
  const baseMap = new Map(baseRows.map((r) => [r.key, r]));
  const rows = [];
  for (let i = 0; i < headRows.length; i++) {
    const head = headRows[i];
    const base = baseMap.get(head.key);
    if (!base) continue;
    rows.push(
      `| ${head.scenario} | ${fmt(base.opsPerSec)} | ${fmt(head.opsPerSec)} | ${pct(base.opsPerSec, head.opsPerSec)} | ${fmt(base.p95, 4)} | ${fmt(head.p95, 4)} | ${pct(base.p95, head.p95)} |`,
    );
  }

  return [
    `### ${title}`,
    '',
    '| Scenario | Base ops/s | Head ops/s | Δ ops/s | Base p95 (ms) | Head p95 (ms) | Δ p95 |',
    '|---|---:|---:|---:|---:|---:|---:|',
    ...(rows.length > 0 ? rows : ['| _No comparable rows_ | - | - | - | - | - | - |']),
    '',
  ].join('\n');
}

async function readReport(path, source) {
  try {
    const raw = await fs.readFile(path, 'utf8');
    return JSON.parse(raw);
  } catch (err) {
    const code = err && typeof err === 'object' && 'code' in err ? err.code : 'unknown';
    return {
      missing: true,
      source,
      reason: `unable to read ${path} (${code})`,
    };
  }
}

async function main() {
  const args = Object.fromEntries(
    process.argv.slice(2).map((a) => {
      const [k, v] = a.split('=');
      return [k, v];
    }),
  );

  const [baseNodeJson, headNodeJson, baseD8Json, headD8Json] = await Promise.all([
    readReport(args['--base-node'], 'base-node'),
    readReport(args['--head-node'], 'head-node'),
    readReport(args['--base-d8'], 'base-d8'),
    readReport(args['--head-d8'], 'head-d8'),
  ]);

  const baseNode = normalizeNode(baseNodeJson);
  const headNode = normalizeNode(headNodeJson);
  const baseD8 = normalizeD8(baseD8Json);
  const headD8 = normalizeD8(headD8Json);

  const issues = [];
  if (baseNodeJson?.missing) issues.push(`- base node missing: ${baseNodeJson.reason}`);
  if (headNodeJson?.missing) issues.push(`- head node missing: ${headNodeJson.reason}`);
  if (baseD8Json?.missing) issues.push(`- base d8 missing: ${baseD8Json.reason}`);
  if (headD8Json?.missing) issues.push(`- head d8 missing: ${headD8Json.reason}`);

  const markdown = [
    '## Benchmark Report (Base vs PR Head)',
    '',
    ...(issues.length > 0
      ? ['### Missing benchmark inputs detected', '', ...issues, '']
      : []),
    table('Node inproc smoke', baseNode, headNode),
    table('d8 inproc', baseD8, headD8),
    '_Generated by `.github/workflows/bench-pr.yml`._',
    '',
  ].join('\n');

  if (args['--out']) {
    await fs.writeFile(args['--out'], markdown);
  } else {
    process.stdout.write(markdown);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
